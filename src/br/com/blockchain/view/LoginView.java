    private void botaoLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLogarActionPerformed
        Usuario user = new Usuario();
        String email = txtEmail.getText();

        if (!this.validarCampos()) {
            JOptionPane.showMessageDialog(null, "Por favor, verifique os campos usuário ou senha.", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
        } else {
            user.setEmail(email);
            user.setPassword(String.valueOf(txtSenha.getPassword()));
            RestServiceImpl rest = new RestServiceImpl();
            ClientResponse response = rest.post("v1/blockchain-api/login", user);

            if (response.getStatus() == HttpStatus.ACCEPTED.value()) {
                UserResponse userResponse = response.getEntity(UserResponse.class);
                JOptionPane.showMessageDialog(null, "Autenticação realizada com sucesso.");

                JFrame janela = new TelaMenu(userResponse);
                janela.setVisible(true);
                janela.setLocationRelativeTo(null);
                janela.setResizable(false);
                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Usuário ou senha inválidos.");
            }
        }
    }//GEN-LAST:event_botaoLogarActionPerformed

