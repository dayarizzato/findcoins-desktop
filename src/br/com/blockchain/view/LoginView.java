    public Boolean validarCampos() {
        Boolean valida = Boolean.TRUE;
        String senha = String.valueOf(txtSenha.getPassword());
        if (txtEmail.getText().equals("")) {
            valida = Boolean.FALSE;
        }
        if (senha.equals("")) {
            valida = Boolean.FALSE;
        }
        return valida;
    }

