    public ClienteCadastro popularCliente(String nome, String sobrenome, String dtNascimento, String cpf, String logradouro,
            String numero, String bairro, String complemento, String cep, String estado, String celular, String sexo, String telefone,
            String pontoReferencia, String email, String senha, String cidade) {
        ClienteCadastro cliente = new ClienteCadastro();
        cliente.setNome(nome);
        cliente.setSobrenome(sobrenome);
        cliente.setDtNascimento(dtNascimento);
        cliente.setRootCpfCnpj(cpf);
        cliente.setCelular(celular);
        cliente.setEmail(email);
        cliente.setPassword(senha);
        cliente.setSexo(sexo.charAt(0));
        cliente.setTelefone(telefone);
        cliente.setEndereco(new Endereco());
        cliente.getEndereco().setLogradouro(logradouro);
        cliente.getEndereco().setNumero(numero);
        cliente.getEndereco().setBairro(bairro);
        cliente.getEndereco().setComplemento(complemento);
        cliente.getEndereco().setCep(cep);
        cliente.getEndereco().setEstado(estado);
        cliente.getEndereco().setPontoReferencia(pontoReferencia);
        cliente.getEndereco().setCidade(cidade);
        return cliente;
    }

