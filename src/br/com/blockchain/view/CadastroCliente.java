    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean erros = false;
        String nome = txtNome.getText();
        String sobrenome = txtSobrenome.getText();
        String dtNascimento = txtDataNascimento.getText();
        String cpf = txtCpf.getText();
        String sexo = null;
        if (radioFeminino.isSelected()) {
            sexo = "F";
        } else if (radioMasculino.isSelected()) {
            sexo = "M";
        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione o sexo");
            erros = true;
        }
        String logradouro = txtLogradouro.getText();
        String numero = txtNumero.getText();
        String bairro = txtBairro.getText();
        String complemento = txtComplemento.getText();
        String cep = txtCep.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        String pontoReferencia = txtPontoReferencia.getText();
        String email = txtEmail.getText();
        String emailConfirm = txtEmailConfirm.getText();
        String senha = String.valueOf(txtSenha.getPassword());
        String senhaConfirm = String.valueOf(txtSenhaConfirm.getPassword());
        String celular = txtCelular.getText();
        String telefone = txtTelefone.getText();

        if (validarCampos(nome, sobrenome, dtNascimento, cpf, logradouro, numero, bairro, complemento, cep, estado, celular, cidade)) {
            JOptionPane.showMessageDialog(null, "Por favor, rever os campos obrigatórios");
            erros = true;
        }
        if (!email.equals(emailConfirm)) {
            JOptionPane.showMessageDialog(null, "O e-mail digitado não confere");
            erros = true;
        }
        if (senha.length() <= 6) {
            JOptionPane.showMessageDialog(null, "A senha deve conter mais de 6 caracteres.");
            erros = true;
        }

        if (!senha.equals(senhaConfirm)) {
            JOptionPane.showMessageDialog(null, "A senha digitada não confere.");
            erros = true;
        }
        
        if(cpf.length() == 14){
            if(!ValidadorUtil.validacpf(cpf)){
                JOptionPane.showMessageDialog(null, "CPF: " + cpf + " Inválido");
                erros = true;
            }
        }else if(cpf.length() == 18){
            if(!ValidadorUtil.validaCnpj(cpf)){
                JOptionPane.showMessageDialog(null, "CNPJ: " + cpf + " Inválido");
                erros = true;
            }
        }else{
            JOptionPane.showMessageDialog(null, "CPF/CNPJ Incorreto, favor utilizar pontos, traços e barra de acordo com o CPF ou CNPJ. ");
            erros = true;
        }
        if (!erros) {
            ClienteCadastro cliente = this.popularCliente(nome, sobrenome, dtNascimento,
                cpf, logradouro, numero, bairro, complemento, cep, estado, celular,
                sexo, telefone, pontoReferencia, email, senha, cidade);

            RestServiceImpl rest = new RestServiceImpl();
            ClientResponse response = rest.post("v1/blockchain-api/cliente", cliente);

            if (response.getStatus() == HttpStatus.CREATED.value()) {
                JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso.");
                this.limparCampos();
                JFrame janela = new LoginView();
                janela.setVisible(true);
                janela.setLocationRelativeTo(null);
                janela.setResizable(false);
                dispose();
            } else if (response.getStatus() == HttpStatus.BAD_REQUEST.value()) {
                String msg = response.getEntity(String.class);
                JOptionPane.showMessageDialog(null, msg);
            } else {
                JOptionPane.showMessageDialog(null, "Erro inesperado.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

